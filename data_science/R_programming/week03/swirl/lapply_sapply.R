## Useful Commands and Tips

## first 6 lines of dataset
head()

## dimensions of dataset
dim()

## complete description of dataset in separate text file
viewinfo()

## lapply(dataset, function, ...)
lapply(flags, class)

## simplified lapply with sapply(dataset, function, ...)
sapply(flags, class)

## get min and max
range()

## unique vector with duplicate removed
unique()
