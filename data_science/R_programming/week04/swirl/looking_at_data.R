## Useful Commands and tips

## get dimensions of data set
dim()

## get number of rows
nrow()

## get number of columns
ncol()

## get size of dataset
object.size()

## get variable/column names
names()

## view top of dataset
head()

## get more or less than first 6
head( , x)

## get last rows
tail( , x)

## get summary of data
summary()

## get more specific column info
table(dataset$column_name)

## concise structure of data or function
str()

